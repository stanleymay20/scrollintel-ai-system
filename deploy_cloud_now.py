def run_command(command, shell=True):
    """Run command and return result"""
    try:
        print(f"ğŸ”§ Running: {command}")
        result = subprocess.run(
            command,
            shell=shell,
            check=True,
            capture_output=True,
            text=True
        )
        print(f"âœ… Success: {command}")
        return result.stdout
    except subprocess.CalledProcessError as e:
        print(f"âŒ Failed: {command}")
        print(f"Error: {e.stderr}")
        return None

def create_render_deployment():
    """Create Render deployment files"""
    
    # Create render.yaml for backend only
    render_config = """services:
  - type: web
    name: scrollintel-backend
    env: python
    plan: starter
    buildCommand: pip install -r requirements.txt
    startCommand: uvicorn scrollintel.api.simple_main:app --host 0.0.0.0 --port $PORT
    healthCheckPath: /health
    envVars:
      - key: ENVIRONMENT
        value: production
      - key: DEBUG
        value: false
      - key: JWT_SECRET_KEY
        generateValue: true
      - key: OPENAI_API_KEY
        value: your-openai-api-key-here
"""
    
    with open("render.yaml", "w") as f:
        f.write(render_config)
    
    print("âœ… Render configuration created")

def create_railway_deployment():
    """Create Railway deployment files"""
    
    # Create railway.json
    railway_config = {
        "build": {
            "builder": "NIXPACKS"
        },
        "deploy": {
            "startCommand": "uvicorn scrollintel.api.simple_main:app --host 0.0.0.0 --port $PORT",
            "healthcheckPath": "/health"
        }
    }
    
    with open("railway.json", "w") as f:
        json.dump(railway_config, f, indent=2)
    
    # Create Procfile
    with open("Procfile", "w") as f:
        f.write("web: uvicorn scrollintel.api.simple_main:app --host 0.0.0.0 --port $PORT\n")
    
    print("âœ… Railway configuration created")

def create_vercel_deployment():
    """Create Vercel deployment files"""
    
    # Create vercel.json
    vercel_config = {
        "version": 2,
        "builds": [
            {
                "src": "scrollintel/api/simple_main.py",
                "use": "@vercel/python"
            }
        ],
        "routes": [
            {
                "src": "/(.*)",
                "dest": "scrollintel/api/simple_main.py"
            }
        ],
        "env": {
            "ENVIRONMENT": "production",
            "DEBUG": "false",
            "OPENAI_API_KEY": "your-openai-api-key-here"
        }
    }
    
    with open("vercel.json", "w") as f:
        json.dump(vercel_config, f, indent=2)
    
    print("âœ… Vercel configuration created")

